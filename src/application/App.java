package application;

public class App {

	public static void main(String[] args) {

		System.out.println(7 + 3);
		System.out.println(9 / 3);
		System.out.println(10 / 3); // Both are int, so int is output
		System.out.println(10.0 / 3.0); // Two floating point values
		System.out.println(9 / 4.0); // One floating point value
		System.out.println(3 * 4);
		System.out.println(3 - 4);
		System.out.println(3.2 - 4);
		System.out.println(3.2 * 4);
		System.out.println(3.2 + 4);

	}

}
